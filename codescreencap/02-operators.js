let a = 10;
let b = 5;

console.log(a + b); // Addition
//output: 15
console.log(a - b); // Subtraction
//output: 5
console.log(a * b); // Multiplication
//output: 50
console.log(a / b); // Division
//output: 2

// Addition
let sum = 10 + 5;
console.log("Sum:", sum); // Output: 15

// Subtraction
let difference = 10 - 5;
console.log("Difference:", difference); // Output: 5

// Multiplication
let product = 10 * 5;
console.log("Product:", product); // Output: 50

// Division
let quotient = 10 / 5;
console.log("Quotient:", quotient); // Output: 2

//COMPARISON OPERATORS
let x = 10;
let y = 5;

console.log(x == y);  // Equal to
console.log(x === y); // Strict equal to
console.log(x != y);  // Not equal to
console.log(x !== y); // Strict not equal to
console.log(x > y);   // Greater than
console.log(x < y);   // Less than
console.log(x >= y);  // Greater than or equal to
console.log(x <= y);  // Less than or equal to

//LOGICAL OPERATORS
let p = true;
let q = false;

console.log(p && q); // Logical AND
console.log(p || q); // Logical OR
console.log(!p);     // Logical NOT

//ASIGNMENT OPERATORS
let num = 10;
num += 5; // Equivalent to: num = num + 5
console.log(num); // Output: 15

let str = "Hello";
str += " World"; // Equivalent to: str = str + " World"
console.log(str); // Output: Hello World

// Addition assignment operator (+=)
let num1 = 10;
num1 += 5; // Equivalent to: num1 = num1 + 5;
console.log("num1:", num1); // Output: 15

// Subtraction assignment operator (-=)
let num2 = 20;
num2 -= 8; // Equivalent to: num2 = num2 - 8;
console.log("num2:", num2); // Output: 12

// Multiplication assignment operator (*=)
let num3 = 7;
num3 *= 3; // Equivalent to: num3 = num3 * 3;
console.log("num3:", num3); // Output: 21

// Division assignment operator (/=)
let num4 = 50;
num4 /= 10; // Equivalent to: num4 = num4 / 10;
console.log("num4:", num4); // Output: 5